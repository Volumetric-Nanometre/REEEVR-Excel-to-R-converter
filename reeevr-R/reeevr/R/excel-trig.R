#' Convert RADIANS
#'
#' @param deg angle in degrees
#' @return angle in radians
#'
#' @examples
#' radians <- excel_radians(180)
#' @export
excel_radians <- function(deg) {

  return(deg * pi / 180)


}

#' Convert DEGREES(a,b)
#'
#' @param rad angle in radians
#' @return angle in degrees
#'
#' @examples
#' degrees <- excel_degrees(pi)
#' @export
excel_degrees <- function(rad) {

  return (rad * 180/ pi)
}


#' Convert ATAN2(a,b)
#'
#' @param a value
#' @param b exponent
#' @return a^b
#'
#' @examples
#' powerSingleVars <- excel_power(2, 3)
#' @export
excel_atan2 <- function(a, b) {

  temp = a^b

  return (temp)
}


